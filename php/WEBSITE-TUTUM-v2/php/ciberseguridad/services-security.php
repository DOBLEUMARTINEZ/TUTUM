            <h2>GAP Analysis</h2>
            <p>
              Nuestro equipo de consultores llevarán a cabo un análisis GAP, el cual es un análisis que mide cómo una organización está llevando a cabo su desempeño de ciberseguridad con respecto a una serie de criterios establecidos tomando como referencia la norma ISO/IEC 27,000:2013, mejores prácticas, controles seleccionado y Frameworks de la industria (MITRE,NIST,etc)​.
              <br>
              El resultado de este análisis establecerá la diferencia entre el desempeño actual y el desempeño deseado.
            </p>
            <br>
            <p>
              El equipo de consultores entregará un cuestionario de levantamiento de información y con base en esa información, programará entrevistas con los responsables y revisará la documentación, procedimientos y políticas del cliente así como su ejecución en la práctica.
            </p>
